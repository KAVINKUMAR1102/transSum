def main():
    st.title("SUMMARIZE AND TRANSLATE")

    text = st.text_area("Enter Text:")
    task = st.selectbox("Choose a Task:", ["Summarize", "Translate"])

    if task == "Summarize":
        max_length = st.number_input("Max Length:", min_value=1, value=150)
        min_length = st.number_input("Min Length:", min_value=1, value=50)
        if st.button("Process Text"):
            result = summarizer(text, max_length=max_length, min_length=min_length, do_sample=False)[0]['summary_text']
            st.subheader("Result:")
            st.write(result)

    elif task == "Translate":
        dest_language = st.selectbox("Choose Destination Language:", list(LANGUAGES.values()))
        dest_code = {v: k for k, v in LANGUAGES.items()}[dest_language]
        if st.button("Process Text"):
            result = translator.translate(text, dest=dest_code).text
            st.subheader("Result:")
            st.write(result)

if __name__ == "__main__":
    main()
